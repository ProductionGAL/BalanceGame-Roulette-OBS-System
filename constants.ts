// Helper to create items quickly
const createItems = (list: string[]): RouletteItem[] => {
    return list.map((text, index) => ({
        id: (index + 1).toString(),
        text: text.trim(),
        color: PRESET_COLORS[index % PRESET_COLORS.length],
        played: false
    }));
};

